require 'rspec'

RSpec.describe 'test' do
  it 'test with "0??\n"' do
    io = IO.popen("ruby agc045/B.rb", "w+")
    io.puts("0??\n")
    io.close_write
    expect(io.readlines.join).to eq("1\n")
  end

  it 'test with "0??0\n"' do
    io = IO.popen("ruby agc045/B.rb", "w+")
    io.puts("0??0\n")
    io.close_write
    expect(io.readlines.join).to eq("2\n")
  end

  it 'test with "??00????0??0????0?0??00??1???11?1?1???1?11?111???1\n"' do
    io = IO.popen("ruby agc045/B.rb", "w+")
    io.puts("??00????0??0????0?0??00??1???11?1?1???1?11?111???1\n")
    io.close_write
    expect(io.readlines.join).to eq("4\n")
  end

end
